-- إنشاء Index على created_at للترتيب السريع
-- هذا يسرع جلب المنتجات المرتبة حسب تاريخ الإنشاء
CREATE INDEX IF NOT EXISTS idx_products_created_at 
ON products(created_at DESC);

-- إنشاء Index على category للفلترة السريعة
-- هذا يسرع البحث عن المنتجات حسب الفئة (أولاد/بنات)
CREATE INDEX IF NOT EXISTS idx_products_category 
ON products(category);

-- إنشاء Composite Index للاستعلامات المعقدة
-- هذا يسرع الاستعلامات التي تفلتر حسب الفئة وترتب حسب التاريخ
CREATE INDEX IF NOT EXISTS idx_products_category_created 
ON products(category, created_at DESC);

-- تحليل الجدول لتحديث إحصائيات المحسن
-- هذا يساعد PostgreSQL على اختيار خطة الاستعلام الأمثل
ANALYZE products;

